public class Main {
    public static void main(String[] args) {
        for (int i = 1; i < 1000; i++) {
            if (isPerfectNumber(i)) {
                System.out.println(">" + i + "<");
            }
        }
    }
    public static boolean isPerfectNumber(int number) {
        int sum = 0;
        for (int i = 1; i < number; i++) {
            if (number % i == 0) {
                sum += i;
            }
        }
        return sum == number;
    }
}